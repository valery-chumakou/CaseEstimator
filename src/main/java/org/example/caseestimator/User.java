package org.example.caseestimator;

public class User {
    private String name;
    private int rate;

    public User(String name, int rate) {
        this.name = name;
        this.rate = this.rate;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
